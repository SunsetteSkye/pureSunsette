; SpeciesNatureData is dex-indexed (slot N = National Dex #N; slot 0 = Missingno), matching
; how the palette tables are indexed (GetPartyMonNatures converts species index -> dex first).
; Each slot is two bytes: PRIMARY nature, then SECONDARY nature (NATURE_NONE if single).

